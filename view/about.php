&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Вірша я тут подав за списком, що його свого часу виконала і переховувала в знайомих моя покійна дружина.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Та в одну й ту саму річку годі ступити вдруге. Як і в даному випадку годі позбутися прикрого відчуття, що демонструєш змуміфіковану дулю в кишені... Мусиш тішитися тим, що від наших учинків лишаються якісь крихти, непідвласні часові й ситуації.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Хоча "Життєпис", як на мене, помітно вирізняється з-поміж інших моїх віршів, та й морочився я з ним довгенько, зараз уже не можу згадати усіх обставин, що спричинилися до його появи. Не виключено, що цей вірш міг бути відчіпним самому собі. Бо десь під ту пору я збирався був написати щось подібне статечною прозою. До останнього мушу вдаватися тепер, щоб прокоментувати деякі позиції.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Бомби із уральської сталі справді падали на українськомовні околиці Києва в ніч на 13 травня 1943-го. Дивне й страшне було це масоване бомбування, що тривало дві з половиною години. Маю і я від нього пам'ятку на все життя: відтоді руки мені тремтять, як у пияка. Німцям згадане бомбування не завдало відчутної шкоди. Натомість загинуло чимало наших людей. Їх поховала німецька окупаційна влада. Одна з братських могил є на Лук'янівському цвинтарі в тій його частині, що межує з Дегтярівською. На гранітному надгробкові був належний напис, який після кількох радянських редакцій остаточно збили в 70-ті роки.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Мій перший поважний контакт з братньою мовою стався за ситуації, коли носієм цієї мови був вояк РОА, якому на п'яну голову забаглося позбиткуватися з мене.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;22 травня 1967-го я опинився серед тих кількох осіб, кого в брутальний спосіб заарештували біля пам'ятника Шевченкові. Але через кілька годин випустили під тиском демонстрації протесту, яка скерувала свій похід до резиденції ЦК КПУ.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Було таке, що люди в цивільному спинили мене біля будинку, де мешкаю. Того дня я саме дістав платню і вертався додому після другої зміни. Ті, що спинили, заходилися показувати службові посвідчення, вибачалися, що погано володіють українською. Зрештою пояснили, що мене підозрюють у грабіжницькому нападі на якусь жінку, і повезли для з'ясування до Подільського РВВС. Там відбувся примітивний і тривалий спектакль, після чого вибачилися й відпустили. Гадаю, що все це знадобилося для того, щоб зробити в моєму помешканні негласний трус.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Якось із друзями довелося перечікувати зливу під надбрамною лаврською церквою. Тут ще були люди, і в тому числі якийсь чоловік із урядовими відзнаками. Серед останніх мені запам'ятався орден Суворова. Мова зайшла за Успенський собор. Хтось чортихнувся на адресу німців. І тоді чоловік із відзнаками обурився, кажучи, що він особисто знав диверсанта, який висадив собор. Коли диверсанта залишали в Києві, хлопці йому шепнули, щоб лише зімітував вибух. Але той, бачте, не послухав... Розповідаючи таке, чоловік крадькома втер сльози, які набігли йому на очі. Окрім мене, свідками цього імпульсивного одкровення були дружина Василя Кобилюха та його шурячка Марта.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Один із слідчих КДБ, читаючи "Життєписа", якого вилучили разом з іншими матеріялами під час трусу 31 березня 1980-го, раптом скипів: "Що ви тут понаписували за Плахотнюка? Ви б послухали, що він тут казав! Він казав, що ми... фашисти!!!". І зараз, мабуть, я не знав би, що відповісти цій нещасній людині в подобі ката.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Про підпал головної книгозбірні України написано чимало. Перебуваючи на курсах, на які щороку посилав військкомат, я почув ще й таке. Йшлося про спеціяльні піротехнічні засоби. Зокрема про шашки, які використовуються в надзвичайній ситуації для знищення штабних документів. Ілюструючи ефективність їхньої дії, лектор згадав книгозбірню, яку, за його словами, було спалено саме такими шашками...<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Один зі списків "Життєпису" КДБ витрусило в Олеся Шевченка. На судовому процесі у Львові цей вірш фігурував серед інших криміналів, що їх інкримінували моїм друзям. Я сам опинився в гротескній ролі свідка. Почесним артистичним званням за виконання згаданої ролі мене не вшанували. Натомість мав підписатися під офіційним попередженням...<BR><BR><BR>

