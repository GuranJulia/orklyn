&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;В тексті висловлюється негативне ставлення автора: "<I>до школи взагалі…</I>", це по його словам: "<I>…моторошна жалюгідність за зовнішньою мішурою…</I>".<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;У відношенні до радянського суспільства автор наклепницьки твердить, що ніби-то: "<I>…це суспільство, ця система приречена - конає бридко і безповоротно і спадщина прийдешнім поколінням насувається страшна. Суспільство деморалізоване, розкладене, загиджене…".<BR>
